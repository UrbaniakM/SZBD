package Entities;

public class Position {
    private String name;
    private int wage;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


    public float getWage() {
        return wage;
    }

    public void setMaxWage(int wage) {
        this.wage = wage;
    }
}
